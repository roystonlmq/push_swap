#include "libft.h"

int	ft_putstr(const char *str)
{
	int	len;

	len = 0;
	if (!str)
		return (write(1, "(null)", 6));
	while (*str)
	{
		write(1, str++, 1);
		len++;
	}
	return (len);
}
